    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!nomeProduto.getText().equals("") && !precoProduto.getText().equals("")){
            nome = nomeProduto.getText();
            codigo = (int) (Math.random() * 1000000);
            preco = Double.parseDouble(precoProduto.getText());
            
            ProdutoDao produtoBd = new ProdutoDaoBd();
            Produto produto = new Produto(nome,codigo,preco);
            
            produtoBd.inserir(produto);
            JOptionPane.showMessageDialog(this,nome + " cadastrado!","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

